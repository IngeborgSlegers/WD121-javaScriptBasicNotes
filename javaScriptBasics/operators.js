// Strictly equal to => ===
// Compares dataType AND value
console.log("3" == 3) //prints true
console.log("3" === 3) //prints false

// Not Equal To => != 
// Compares just the values
console.log(3 != "3") //prints false - Send help brain is broken
console.log(3 != 4) //prints true - because three is not equal to four

// Strictly Not Equal to => !==
// Compares values and data types

console.log(3 !== "3"); //prints true

// Greater than 
// >
console.log(4 > 3); //prints true

// Greater than or equal to
// >=
console.log(3 >= 2) //prints true

// Less than
// <
console.log(2 < 3) //prints true

// Less than or equal to
// <=
console.log(2 <= 3) //prints true

